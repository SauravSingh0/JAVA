//This program demonstrates the use of if-else statements in Java.
public class elseif {
    public static void main(String[] args) {
        int x = 20;
        if(x ==20){
            System.out.println("x is 20.");
        }
        else if (x>25){
            System.out.println("x is greater than 25.");
        }
        else {
            System.out.println("x is smaller than 20.");
        }
    }
}
